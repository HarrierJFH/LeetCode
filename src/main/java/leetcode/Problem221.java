    public int maximalSquare(char[][] matrix) {
        int height = matrix.length;
        if (height == 0) {
            return 0;
        }
        int width = matrix[0].length;
        if (width == 0) {
            return 0;
        }
        if (height == 1) {
            for (int i = 0; i < width; i++) {
                if (matrix[0][i] == '1') {
                    return 1;
                }
            }
            return 0;
        }
        if (width == 1) {
            for (int i = 0; i < height; i++) {
                if (matrix[i][0] == '1') {
                    return 1;
                }
            }
            return 0;
        }
        int[][] areas = new int[height][width];
        int h, w, length = 0, layer = 2, a;
        for (int i = height - 2; i >= 0; i--) {
            h = i;
            w = 0;
            for (; ; ) {
                if (matrix[h][w] == '1') {
                    a = areas[h + 1][w + 1] + 1;
                    if (a == 1 && matrix[h + 1][w + 1] == '1') {
                        a = 2;
                    }
                    if (a == 1) {
                        if (length == 0) {
                            length = 1;
                        }
                        areas[h][w] = 1;
                    } else {
                        a = checkSquare(matrix, h, w, a);
                        if (a > length) {
                            length = a;
                            if (length >= width || length >= height) {
                                return length * length;
                            }
                        }
                        areas[h][w] = a;
                    }
                }
                if (w + layer >= width) {
                    if (h == 0) {
                        layer++;
                        break;
                    } else {
                        h--;
                    }
                } else {
                    w++;
                }
            }
        }

        if (length == 0) {
            h = height - 1;
            w = 0;
            for (; ; ) {
                if (matrix[h][w] == '1') {
                    return 1;
                }
                if (w + 1 == width) {
                    if (h == 0) {
                        break;
                    } else {
                        h--;
                    }
                } else {
                    w++;
                }
            }
        }
        return length * length;
    }

    private int checkSquare(char[][] matrix, int height, int width, int area) {
        for (int i = 1; i < area; i++) {
            if (matrix[height][width + i] == '0' || matrix[height + i][width] == '0') {
                return i;
            }
        }
        return area;
    }

